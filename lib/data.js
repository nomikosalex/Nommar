// Pick the language index of a value that may be an [EN, GR] tuple (or a plain string).
const pick = (v, i) => (Array.isArray(v) ? v[i] : v);
const langIndex = (lang) => (lang === 'gr' ? 1 : 0);

export function translate(lang) {
  const i = langIndex(lang);
  const out = {};
  for (const k in STRINGS) out[k] = STRINGS[k][i];
  return out;
}

// URL-safe slug from a service/package name (used for /book?service=… links + seed).
export function slugify(s) {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

// ---- Pricing ----
export function priceLabel(priceMode = 'from', lang = 'en') {
  const gr = lang === 'gr';
  if (priceMode === 'onRequest') return gr ? 'Τιμή κατόπιν αιτήματος' : 'Price on request';
  if (priceMode === 'dash') return '—';
  return gr ? 'Από €—' : 'From €—';
}

